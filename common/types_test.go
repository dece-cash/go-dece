func TestAddressUnmarshalJSON(t *testing.T) {
	var tests = []struct {
		Input     string
		ShouldErr bool
		Output    *big.Int
	}{
		{"", true, nil},
		{`""`, true, nil},
		{`"00"`, true, nil},
		{`"ll"`, true, nil},
		{`"61lgYuqehrEHDEeBAtjWKPdfnTREcok2yKAMoeS1afZENdh7A12k6vW2d9xb6bu73RJVhRt1yjtd1LwREtx67UpN"`, true, nil},
		{`"1111111111111111111111111111111111111111111111111111111111111111"`, false, big.NewInt(0)},
		{`"111111111111111111111111111111111111111111111111111111111111111H"`, false, big.NewInt(16)},
	}
	for i, test := range tests {
		var v Address
		err := json.Unmarshal([]byte(test.Input), &v)
		if err != nil && !test.ShouldErr {
			t.Errorf("test #%d: unexpected error: %v", i, err)
		}
		if err == nil {
			if test.ShouldErr {
				t.Errorf("test #%d: expected error, got none", i)
			}
			if v.Big().Cmp(test.Output) != 0 {
				t.Errorf("test #%d: address mismatch: have %v, want %v", i, v.Big(), test.Output)
			}
		}
	}
}

